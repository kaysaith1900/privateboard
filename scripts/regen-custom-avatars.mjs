/**
 * Regenerate avatars for all custom (user-created) directors using the
 * BRICK pet generator in public/avatar-skill.js.
 *
 * Use this AFTER swapping the avatar generator so existing custom
 * agents (whose avatars were drawn by the previous chibi generator)
 * get the new BRICK look. Seeds by `agents.id` so the result is
 * deterministic per agent — re-running yields the same artwork.
 *
 * SAFETY ───────────────────────────────────────────────────────────────
 *   This OVERWRITES `agents.avatar_path` for every custom director.
 *   Users who manually picked a specific look via the profile menu
 *   will lose that choice (but can click Regenerate again to roll a
 *   fresh seed). Run with --dry-run first and back up state.db.
 *
 * SKIPS:
 *   · The chair (role_kind='moderator') — its avatar is server-locked
 *   · Seed directors (avatar_path starts with "/avatars/") — those use
 *     the static SVG files regenerated by scripts/regen-avatars.mjs
 *
 * USAGE:
 *   cp ~/.boardroom/state.db ~/.boardroom/state.db.bak
 *   node scripts/regen-custom-avatars.mjs --dry-run
 *   node scripts/regen-custom-avatars.mjs
 *
 *   # Custom DB location:
 *   BOARDROOM_DIR=/path/to/dir node scripts/regen-custom-avatars.mjs
 */
import { readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { homedir } from "node:os";
import Database from "better-sqlite3";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = dirname(__dirname);

// ── Resolve DB path (mirrors src/utils/paths.ts) ─────────────────────
const baseDir = (process.env.BOARDROOM_DIR && process.env.BOARDROOM_DIR.trim())
  || join(homedir(), ".boardroom");
const dbPath = join(baseDir, "state.db");

// ── Load AvatarSkill via eval (same trick as regen-avatars.mjs) ──────
const skillPath = join(repoRoot, "public", "avatar-skill.js");
const skillSource = readFileSync(skillPath, "utf8");
globalThis.window = globalThis.window || globalThis;
// eslint-disable-next-line no-eval
eval(skillSource);
const AvatarSkill = globalThis.window.AvatarSkill;
if (!AvatarSkill || typeof AvatarSkill.generateDataUrl !== "function") {
  throw new Error("avatar-skill.js did not expose window.AvatarSkill.generateDataUrl");
}

const dryRun = process.argv.includes("--dry-run");

// ── Open DB ──────────────────────────────────────────────────────────
console.log(`db: ${dbPath}`);
console.log(`mode: ${dryRun ? "DRY RUN (no writes)" : "LIVE (will UPDATE rows)"}\n`);

// Dry-run opens read-only so it can coexist with a running dev server / Electron.
// LIVE mode requires the DB writer to be free — close the dev app first.
const db = new Database(dbPath, dryRun ? { readonly: true, fileMustExist: true } : {});
if (!dryRun) db.pragma("journal_mode = WAL");

// ── Find candidates ──────────────────────────────────────────────────
const rows = db.prepare(`
  SELECT id, name, role_kind, avatar_path
  FROM agents
  ORDER BY created_at ASC
`).all();

let skippedChair = 0;
let skippedSeed = 0;
let toUpdate = [];

for (const r of rows) {
  if (r.role_kind === "moderator") { skippedChair++; continue; }
  if (typeof r.avatar_path === "string" && r.avatar_path.startsWith("/avatars/")) {
    skippedSeed++;
    continue;
  }
  toUpdate.push(r);
}

console.log(`scanned ${rows.length} agent(s)`);
console.log(`  · skipped chair       : ${skippedChair}`);
console.log(`  · skipped seed dirs   : ${skippedSeed}`);
console.log(`  · custom to regen     : ${toUpdate.length}\n`);

if (toUpdate.length === 0) {
  console.log("nothing to do.");
  closeDb();
  process.exit(0);
}

// ── Regenerate ───────────────────────────────────────────────────────
const stmt = db.prepare("UPDATE agents SET avatar_path = ?, updated_at = ? WHERE id = ?");
const now = Date.now();

let touched = 0;
for (const r of toUpdate) {
  const dataUrl = AvatarSkill.generateDataUrl(r.id);
  if (dryRun) {
    console.log(`would update: ${r.id.padEnd(24)} (${r.name})`);
  } else {
    stmt.run(dataUrl, now, r.id);
    touched++;
    console.log(`✓ ${r.id.padEnd(24)} (${r.name})`);
  }
}

console.log(`\n${dryRun ? "would update" : "updated"} ${dryRun ? toUpdate.length : touched} agent(s)`);

// ── WAL checkpoint + close (per CLAUDE.md WAL rule) ──────────────────
function closeDb() {
  try {
    db.pragma("wal_checkpoint(TRUNCATE)");
  } catch (e) {
    console.error("checkpoint failed:", e.message);
  }
  db.close();
}

closeDb();
