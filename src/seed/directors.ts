/**
 * The default director catalog. Each ships a system prompt built from the
 * canonical 8-section template (identity / intellectual lineage /
 * load-bearing concepts / method / referent set / voice / boundaries /
 * failure modes) — the same shape new directors are generated against
 * via `agent-spec.ts`. Keeping seeds and generated specs structurally
 * identical means downstream code (composer rationale, brief Stage 3,
 * UI profile renderer) sees one consistent agent surface.
 *
 * Two universal rules apply to every director:
 *
 *   · ANTI_FLATTER   · the prefacing-with-affirmation rule (every
 *                      director gets the same anti-flatter line in
 *                      Boundaries — without this, RLHF-trained models
 *                      open every turn with "Great question!" then
 *                      pivot, blunting the room's adversarial axis).
 *
 *   · TURN_DIRECTIVE · every turn must introduce a new variable,
 *                      constraint, analogy, or counter-example. If
 *                      the director can only deepen along the existing
 *                      direction, they must switch to another lens.
 *                      This is the hard rule that prevents N-direc-
 *                      tor rooms from collapsing into one director's
 *                      argument iterated N ways.
 *
 * IDs are stable handles (no nanoid) so config files / demo seeds /
 * docs can reference them without indirection.
 */
